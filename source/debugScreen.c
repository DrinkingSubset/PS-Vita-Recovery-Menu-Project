static void set_framebuf(uint32_t *fb) {
    SceDisplayFrameBuf info;
    info.size        = sizeof(SceDisplayFrameBuf);
    info.base        = fb;
    info.pitch       = FB_STRIDE;
    info.pixelformat = SCE_DISPLAY_PIXELFORMAT_A8B8G8R8;
    info.width       = SCREEN_W;
    info.height      = SCREEN_H;
    sceDisplaySetFrameBuf(&info, SCE_DISPLAY_SETBUF_NEXTFRAME);
}

void psvDebugScreenInit(void) {
    // Allocate two framebuffers from CDRAM
    for (int i = 0; i < 2; i++) {
        g_uid[i] = sceKernelAllocMemBlock("dbg_fb",
                       SCE_KERNEL_MEMBLOCK_TYPE_USER_CDRAM_RW,
                       FB_SIZE, NULL);
        if (g_uid[i] < 0) {
            // Fallback to regular RAM if CDRAM fails
            g_uid[i] = sceKernelAllocMemBlock("dbg_fb",
                           SCE_KERNEL_MEMBLOCK_TYPE_USER_RW,
                           FB_SIZE, NULL);
        }
        sceKernelGetMemBlockBase(g_uid[i], (void **)&g_fb[i]);
        memset(g_fb[i], 0, FB_SIZE);
    }

    g_back = 0;

    // Show buffer 1 (front), draw to buffer 0 (back)
    set_framebuf(g_fb[1]);

    cur_x  = 0;
    cur_y  = 0;
    fg_col = 0xFFFFFFFF;
    bg_col = 0xFF000000;
}

void psvDebugScreenClear(uint32_t color) {
    if (!g_fb[g_back]) return;
    uint32_t *buf = g_fb[g_back];
    for (int i = 0; i < FB_STRIDE * SCREEN_H; i++)
        buf[i] = color;
    cur_x = 0;
    cur_y = 0;
}

void psvDebugScreenSetFgColor(uint32_t color) { fg_col = color; }
void psvDebugScreenSetBgColor(uint32_t color) { bg_col = color; }
void psvDebugScreenSetCoordsXY(int x, int y)  { cur_x = x; cur_y = y; }

void psvDebugScreenFlip(void) {
    // Wait for vblank to prevent tearing
    sceDisplayWaitVblankStart();
    // Show the back buffer we just drew to
    set_framebuf(g_fb[g_back]);
    // Swap: next draw goes to the other buffer
    g_back ^= 1;
}

/* bg == PSV_TRANSPARENT: only draw foreground pixels, leave bg intact */
#define PSV_TRANSPARENT 0x00000000u

static void draw_char_at(int x, int y, uchar c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 127) c = 32;
    const uchar *glyph = font[c - 32];
    uint32_t *buf = g_fb[g_back];
    int transparent = (bg == PSV_TRANSPARENT);
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) {
            int lit = (glyph[row] & (0x80 >> col)) != 0;
            uint32_t color = lit ? fg : bg;
            /* 2x scale — each glyph pixel becomes a 2x2 block */
            for (int dy = 0; dy < 2; dy++) {
                for (int dx = 0; dx < 2; dx++) {
                    int px = x + col*2 + dx;
                    int py = y + row*2 + dy;
                    if (px < 0 || px >= SCREEN_W || py < 0 || py >= SCREEN_H) continue;
                    if (lit)
                        buf[py * FB_STRIDE + px] = fg;
                    else if (!transparent)
                        buf[py * FB_STRIDE + px] = bg;
                }
            }
        }
    }
}

int psvDebugScreenPrintf(const char *fmt, ...) {
    if (!g_fb[g_back]) return 0;
    char buf[512];
    va_list args;
    va_start(args, fmt);
    int len = vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);

    for (int i = 0; i < len; i++) {
        uchar c = (uchar)buf[i];
        if (c == '\n') {
            cur_x = 0;
            cur_y += 16;
            if (cur_y + 16 > SCREEN_H) cur_y = 0;
            continue;
        }
        if (cur_x + 16 > SCREEN_W) {
            cur_x = 0;
            cur_y += 16;
            if (cur_y + 16 > SCREEN_H) cur_y = 0;
        }
        draw_char_at(cur_x, cur_y, c, fg_col, bg_col);
        cur_x += 16;
    }
    return len;
}

void psvDebugScreenFillRect(int x, int y, int w, int h, uint32_t color) {
    if (!g_fb[g_back]) return;
    uint32_t *buf = g_fb[g_back];
    int x1 = x < 0 ? 0 : x;
    int y1 = y < 0 ? 0 : y;
    int x2 = (x + w) > SCREEN_W ? SCREEN_W : (x + w);
    int y2 = (y + h) > SCREEN_H ? SCREEN_H : (y + h);
    for (int py = y1; py < y2; py++)
        for (int px = x1; px < x2; px++)
            buf[py * FB_STRIDE + px] = color;
}
